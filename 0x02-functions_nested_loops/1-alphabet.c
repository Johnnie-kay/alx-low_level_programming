#include "main.h"
/**
 * main - Entry point
 * print_alphabet - prints alphabets in lowercase
 * Description: It prints the alphabet in lowercase fallowed by a new line
 * Return: 0
 */

int main(void)
{
void print_alphabet(void)
{
	char i;

	for (i = 'a'; i <= 'z'; i++)
		_putchar(i);
	_putchar('\n');
}
}
